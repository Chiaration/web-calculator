//Adding Function
function add(a,b) {
    return a+b;
}

//Substracting Function
function subtract(a,b) {
    return a - b;
}

//Multiplying Function
function multiply(a,b) {
    return a*b;
}

//Dividing Function
function divide(a, b) {
    if (b === 0) {
        return "ERROR"
    } else {
        return a/b;
    }
}